function compareIPAddrToPrefix(wholeAddr, thePrefix)
{
	let slashNum = thePrefix.indexOf("*");
	let prefOnly = thePrefix.slice(0,slashNum);
	let addrTryMatch = wholeAddr.slice(0,slashNum);
	if(addrTryMatch === prefOnly)
		return 0;
	else if(addrTryMatch < prefOnly)
		return -1;
	else
		return 1;
}

function IPStringToBinString(ipAddr)
{
	let allFourAddrOctets = ipAddr.split(".");
	let res = "";
	
	let i;
	for(i=0;i<4;i++)
		res += ("00000000" + (allFourAddrOctets[i] >>> 0).toString(2)).slice(-8);

	return res;
}

