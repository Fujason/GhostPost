function currentlyOnChineseIP(theCallback)
{
	chrome.storage.local.get(["gpOnChineseIP", "gpOnChineseIPChecked"], function(results)
	{
		let thisBrowserSessionOnChineseIP_defined = results["gpOnChineseIPChecked"];
		let thisBrowserSessionOnChineseIP = results["gpOnChineseIP"];
		if(!thisBrowserSessionOnChineseIP_defined)
		{
			let canhazipXHR = new XMLHttpRequest();
			canhazipXHR.open("GET", "http://canihazip.com/s", true);
			canhazipXHR.onreadystatechange = function()
			{
				// If the request completed
				if (canhazipXHR.readyState == 4) 
				{
					if (canhazipXHR.status == 200)//success
						thisBrowserSessionOnChineseIP = testIfChineseIP(canhazipXHR.responseText);
					else
						thisBrowserSessionOnChineseIP = true; //assume in China to be safe
					chrome.storage.local.set({"gpOnChineseIPChecked": true, "gpOnChineseIP": thisBrowserSessionOnChineseIP});
					theCallback(thisBrowserSessionOnChineseIP);
				}
			};
			canhazipXHR.timeout = 12000;
			canhazipXHR.ontimeout = function() 
			{
				thisBrowserSessionOnChineseIP = true; //assume in China to be safe
				chrome.storage.local.set({"gpOnChineseIPChecked": true, "gpOnChineseIP": thisBrowserSessionOnChineseIP});
				theCallback(thisBrowserSessionOnChineseIP);
			};

			canhazipXHR.send();
		}
		else
			theCallback(thisBrowserSessionOnChineseIP);
	});
}
